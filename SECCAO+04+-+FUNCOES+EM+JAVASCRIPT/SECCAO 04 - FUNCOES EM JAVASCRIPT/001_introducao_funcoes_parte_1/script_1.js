/* 
Funções em JavaScript

Uma função é um bloco de código que pode ser chamado em qualquer parte do seu código.
Trata-se de uma forma muito comum de organizar o código, permitindo que escreva uma vez
uma determinada lógica e a reutilize em diferentes partes da sua aplicação.

Uma função pode receber parâmetros, que são valores que pode passar para a função quando a chama.
Ela pode retornar um valor, que é o resultado da execução da função.
*/

// definir uma função
function myFunction(message) {
  console.log(message);
}

// o código dentro da função não será executado até que a função seja chamada

// chamar a função
myFunction("Pedro");

// esta função não recebe quaisquer parâmetros
