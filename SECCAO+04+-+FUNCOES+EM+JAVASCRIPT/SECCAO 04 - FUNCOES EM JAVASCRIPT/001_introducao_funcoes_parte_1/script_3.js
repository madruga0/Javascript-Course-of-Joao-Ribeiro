// mais do que um parâmetro
function displayFullName(firstName, lastName) {
    console.log(firstName + " " + lastName);
}

// chamar a função e passar valores para os parâmetros
displayFullName("John", "Lennon");
displayFullName("Paul", "McCartney");
displayFullName("Ringo", "Starr");
displayFullName("George", "Harrison");

// NOTA: parâmetros vs argumentos
// parâmetros são as variáveis que definimos na função
// argumentos são os valores que passamos para a função quando a chamamos