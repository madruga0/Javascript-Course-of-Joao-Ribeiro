function sum(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

// agora vamos criar duas variáveis numéricas e usar as funções para realizar as operações

let x = 10;
let y = 5;

console.log(sum(x, y));         // 15
console.log(subtract(x, y));    // 5
console.log(multiply(x, y));    // 50
console.log(divide(x, y));      // 2