// Tal como acontece noutras linguagens de programação, o JavaScript
// também tem funções integradas que podem ser usadas para realizar operações comuns.
// Estas funções são parte do objeto global e podem ser usadas sem a necessidade de
// declarar ou importar nada.

// Funções Matemáticas
console.log(Math.PI); // exibe 3.141592653589793
console.log(Math.round(4.7)); // exibe 5
console.log(Math.round(4.4)); // exibe 4
console.log(Math.pow(8, 2)); // exibe 64
console.log(Math.sqrt(64)); // exibe 8
// ...

// Funções de Data
const date = new Date();
console.log(date); // exibe a data atual
console.log(date.getFullYear()); // exibe o ano atual
console.log(date.getMonth()); // exibe o mês atual
console.log(date.getDay()); // exibe o dia atual

// Funções do Console
console.log("Hello World!"); // exibe uma mensagem no console
console.error("Error!"); // exibe uma mensagem de erro no console
console.warn("Warning!"); // exibe uma mensagem de aviso no console
console.info("Information!"); // exibe uma mensagem de informação no console
console.table(["John", "Mary", "Peter", "Joao"]); // exibe uma tabela no console

