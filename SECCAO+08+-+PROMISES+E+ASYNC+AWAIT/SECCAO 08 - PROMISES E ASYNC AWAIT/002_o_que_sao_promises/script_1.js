/* 
Por definição, uma promessa (promise) é um objeto que encapsula o resultado de uma operação assíncrona.
As promises têm três estados: pendente, cumprida e rejeitada.

- Pendente: O estado inicial de uma promessa. Indica que a operação assíncrona ainda não foi concluída.
A operação ainda está em progresso.

- Cumprida: A operação foi concluída com sucesso. A promessa tem agora um valor resolvido.

- Rejeitada: A operação falhou. A promessa tem um motivo para a falha.

- Uma promessa começa sempre no estado pendente. Pode depois transitar para o estado cumprida ou rejeitada.

Agora que sabemos o que é uma promessa, vamos ver como podemos criar uma.
*/
