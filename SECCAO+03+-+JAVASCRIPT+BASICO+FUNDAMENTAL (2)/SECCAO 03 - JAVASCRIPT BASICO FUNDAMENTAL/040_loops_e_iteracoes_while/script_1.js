// Um outro tipo de loop é o while.
// O loop while executa um bloco de código enquanto uma condição for verdadeira.

let index = 0;

while (index < 10) {
    console.log(index);
    index++;
}

// Neste caso, a variável de contagem é declarada fora do loop e incrementada dentro do loop
// este é um padrão muito comum
// CUIDADO: se você esquecer de incrementar a variável de contagem, o loop será executado indefinidamente