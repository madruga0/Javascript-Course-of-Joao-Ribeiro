// as variáveis podem ser inicializadas com o valor de outra variável
let myName = "Steve";
let anotherName = myName;

console.log(myName);          // Steve
console.log(anotherName);   // Steve

let number1 = 50;
let number2 = number1 + 50;

console.log(number1);       // 50
console.log(number2);       // 100

let num1 = 50;
let num2 = 100;
let total = num1 + num2;

console.log(total);         // 150

