// No JavaScript as variáveis são definidas com a palavra-chave let, 
// seguida do nome da variável e o valor que ela irá armazenar.
// Dependendo do valor, uma variável pode ser de vários tipos, como string, number, boolean, etc.


// valores de string podem ser definidos usando aspas simples ou duplas:
let myString1 = 'Hello World';
// ou
let myString2 = "Hello World";

// podemos usar crase (Acento grave) para definir uma string:
let myString3 = `Hello World`;

// podemos usar aspas simples ou duplas dentro de uma string definida com crase:
let fullName = `John "Amazing" Doe`;

// podemos definir várias strings em uma única linha, separando-as com vírgulas:
let firstName = 'John', lastName = 'Doe';

// o valor de uma string não mostrará as aspas:
console.log(firstName);

/*
    Nota: o uso de aspas simples ou duplas é indiferente, desde que sejam usadas de forma consistente.
    O uso de crase é diferente, pois permite usar variáveis dentro de uma string.
    Veremos como isso funciona mais tarde ao longo do curso.
*/
