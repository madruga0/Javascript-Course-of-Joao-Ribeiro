// Um dos tipos de dados mais importantes em JavaScript é o tipo de dado 'object' (objeto).
// Os objetos são estruturas de dados que podem armazenar coleções de dados e funcionalidades.
// Vamos introduzir aqui o conceito de objetos, mas não te preocupes, pois iremos aprofundar mais
// sobre este assunto em aulas futuras.

// definindo um objeto
let myObject = {
    name: 'John',       // propriedade 'name' do objeto (tipo string)
    age: 31,            // propriedade 'age' do objeto (tipo number)
    isMarried: false    // propriedade 'isMarried' do objeto (tipo boolean)
};

// apresentando o objeto completo
// console.log(myObject);  // { name: 'John', age: 31, isMarried: false }

// apresentar o valor de uma propriedade do objeto
console.log(myObject.name); // John

