// O tipo de dados booleano é um dos tipos de dados primitivos em JavaScript
// Trata-se de um tipo que pode ter apenas dois valores: true ou false (verdadeiro ou falso).
// O tipo booleano é frequentemente utilizado para controlar o fluxo de execução do código,
// como em estruturas condicionais e loops, que veremos mais adiante.

