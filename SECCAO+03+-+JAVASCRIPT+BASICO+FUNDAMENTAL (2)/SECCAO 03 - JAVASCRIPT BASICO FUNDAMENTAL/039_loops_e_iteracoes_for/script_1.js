// O loop for é uma estrutura de repetição que permite executar
// um bloco de código várias vezes, com base em uma condição.

// A sintaxe básica do loop for é a seguinte:
// for (inicialização; condição; incremento) {
//     // bloco de código a ser executado
// }

for (let i = 0; i < 10; i++) {
  console.log(i);
}

// este loop irá rodar 10 vezes
// em cada iteração, o valor de i irá aumentar em 1
// o primeiro elemento do loop é a variável contadora
// o segundo elemento é a condição
// o terceiro elemento é o incremento

// podes usar o valor do contador dentro do ciclo
for (let index = 0; index < 10; index++) {
  console.log(index);
}
