// statements - uma statement é uma instrução de código que executa uma ação.

// definir uma variável é uma statement ou instrução
let text = "Olá Mundo!";

// chamar uma função é uma statement ou instrução
console.log(text);

// instruções podem ser separadas por ponto e vírgula
console.log("Um"); console.log("Dois"); console.log("Três");

// instruções podem ser separadas por quebras de linha
console.log("Um")
console.log("Dois")
console.log("Três")

// as instruções podem ser separadas por ponto e vírgula e quebras de linha
console.log("One");
console.log("Two");
console.log("Three");

// IMPORTANTE:
// Não usar ponto e vírgula é possível (mas não recomendado).
// Essa técnica de ausência de ponto e vírgula é chamada de ASI - Automatic Semicolon Insertion
// O JavaScript tenta inserir ponto e vírgula automaticamente quando necessário.