// Os tipos de dados 'null' e 'undefined' são dois tipos de dados primitivos em JavaScript
// que representam a ausência de valor ou a falta de definição de uma variável.

let myNull = null;  

console.log(myNull); // null

let myUndefined = undefined;    // undefined pode ser atribuído a uma variável, mas não é recomendado

console.log(myUndefined); // undefined

// undefined significa que ainda não foi atribuído um valor à variável

let myNumber;   // a variável 'myNumber' não tem nenhum valor atribuído ainda

console.log(myNumber);  // undefined

// Veremos mais sobre os tipos de dados 'null' e 'undefined' no futuro, quando estudarmos conceitos mais avançados,
// como funções, objetos e arrays.