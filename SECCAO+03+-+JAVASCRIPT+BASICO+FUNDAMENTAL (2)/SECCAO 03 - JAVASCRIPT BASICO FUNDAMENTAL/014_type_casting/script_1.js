// Type Casting é o processo de conversão de um tipo de dado para outro tipo de dado.
// Este processo é necessário quando queremos realizar operações entre tipos de dados diferentes.
// Existem dois tipos de type casting: o type casting implícito e o type casting explícito.

// converter um número para string
let score = 100;
console.log(score.toString());
// ou
console.log(String(score));

// converter um booleano para string
let isShow = true;
console.log(isShow.toString());
// ou
console.log(String(isShow)); // 'true'
