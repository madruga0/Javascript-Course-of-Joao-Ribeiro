// Os operadores de comparação são usados para comparar dois valores.
// Eles retornam um valor booleano (true ou false) dependendo do resultado da comparação.
// São muito importantes para a lógica de programação, especialmente em estruturas 
// condicionais e loops que veremos mais adiante neste curso.
// É muito importante entender como funcionam os operadores de comparação,
// pois eles são fundamentais para a lógica de programação e são usados em todos os projetos.

let num_1 = 5;
let num_2 = 10;

// operador de comparação (==) 
console.log(num_1 == num_2);    // false

// operador de negação de comparação (!=)
console.log(num_1 != num_2);    // true

// operador de comparação maior que (>)
console.log(num_1 > num_2);     // false

// operador de comparação maior ou igual que (>=)
console.log(num_1 >= num_2);    // false

// operador de comparação menor que (<)
console.log(num_1 < num_2);     // true

// operador de comparação menor ou igual que (<=)
console.log(num_1 <= num_2);    // true

// operador de comparação estritamente igual (===)
console.log(num_1 === num_2);   // false

// operador de comparação estritamente diferente (!==)
console.log(num_1 !== num_2);   // true

// NOTA: O operador de comparação estritamente igual (===) e estritamente diferente (!==)
// compara o valor e o tipo dos valores comparados.
