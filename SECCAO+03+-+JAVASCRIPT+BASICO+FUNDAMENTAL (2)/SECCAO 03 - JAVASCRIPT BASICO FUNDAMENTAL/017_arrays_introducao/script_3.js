// array de strings
let names = ['John', 'Mary', 'Bob'];

// adicionar um novo elemento ao array
names.push('Peter');    // ['John', 'Mary', 'Bob', 'Peter']

// remover o último elemento do array
names.pop();            // ['John', 'Mary', 'Bob']

// adicionar um novo elemento ao início do array
names.unshift('Peter'); // ['Peter', 'John', 'Mary', 'Bob']

// remover o primeiro elemento do array
names.shift();          // ['John', 'Mary', 'Bob']

// existem outros métodos para adicionar e remover elementos de um array
// mas estes são os mais comuns. Vamos ver mais tarde outros métodos para manipular arrays
// e também como iterar sobre os elementos de um array