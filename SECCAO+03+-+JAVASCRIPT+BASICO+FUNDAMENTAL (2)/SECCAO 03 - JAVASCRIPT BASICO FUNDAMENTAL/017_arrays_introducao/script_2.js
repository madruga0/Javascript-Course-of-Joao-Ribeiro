// array de strings
let names = ['John', 'Mary', 'Bob'];

// buscar o primeiro elemento do array
console.log(names[0]); // John

// os índices do array começam em 0 - zero
// então, se um array tem 3 elementos, o último elemento estará no índice 2
console.log(names[2]); // Bob

// se tentarmos acessar um elemento que não existe, teremos undefined
console.log(names[3]); // undefined

