// Os operadores aritméticos são usados para realizar operações matemáticas em JavaScript.
// Eles são usados para realizar cálculos e manipular números.
// Os operadores aritméticos mais comuns são:
// + (adição), - (subtração), * (multiplicação), / (divisão), % (módulo) e ** (exponenciação).

// duas variáveis com valores numéricos
let num_1 = 10;
let num_2 = 5;

// adição
let sum = num_1 + num_2;                // 10 + 5 = 15

// subtração
let subtraction = num_1 - num_2;        // 10 - 5 = 5

// multiplicação
let multiplication = num_1 * num_2;     // 10 * 5 = 50

// divisão
let division = num_1 / num_2;           // 10 / 5 = 2

// exponenciação (potência)
let exponentiation = num_1 ** num_2;    // 10 ** 5 = 100000

// módulo (resto da divisão)
let modulus = num_1 % num_2;            // 10 % 5 = 0
let modulus_2 = 11 % 5;                 // 11 % 5 = 1 (11 / 5 = 2, resto 1)